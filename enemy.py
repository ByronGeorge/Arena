

class Enemy:

    def __init__(self, health, mana, armor, magicResistance, magicDamage, physicalDamage, enemyType, enemyName, dexterity, level):
        self.health = health
        self.mana = mana
        self.armor = armor
        self.magicResistance = magicResistance
        self.magicDamage = magicDamage
        self.physicalDamage = physicalDamage
        self.enemyType = enemyType
        self.enemyName = enemyName
        self.dexterity = dexterity
        self.level = level
